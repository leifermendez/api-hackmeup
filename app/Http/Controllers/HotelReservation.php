    /**
     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $fields = array();
        foreach ($request->all() as $key => $value) {
            $fields[$key] = $value;
        }
        try {

            $data = hotelReservations::insertGetId($fields);
            $data = hotelReservations::find($data);

            $response = array(
                'status' => 'success',
                'msg' => 'Insertado',
                'data' => $data,
                'code' => 0
            );
            return response()->json($response);
        } catch (\Exception $e) {
            $response = array(
                'status' => 'fail',
                'code' => 5,
                'error' => $e->getMessage()
            );
            return response()->json($response);
        }
    }


    /**
     * Display the specified resource.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function show(Request $request, $id)
    {
        try {
            $limit = ($request->limit) ? $request->limit : 15;
            $last_name = $request->last_name;

            if(!$id){
                return response()->json([
                 'msg' => 'ID no valido'
                ],404); 
             }

            if(!$last_name){
                return response()->json([
                 'msg' => 'Apellido no valido'
                ],404); 
             }
             

             $data = hotelReservations::orderBy('hotel_reservations.id', 'DESC')
             ->join('hotel_availables','hotel_reservations.hotel_available','=','hotel_availables.id')
             ->where('hotel_reservations.id',$id)
             ->where('hotel_reservations.last_name',$last_name)
             ->select('hotel_reservations.*',
             'hotel_availables.name as hotel_name'
             )
             ->paginate($limit);

            $response = array(
                'status' => 'success',
                'data' => $data,
                'code' => 0
            );
            return response()->json($response);

        } catch (\Exception $e) {

            $response = array(
                'status' => 'fail',
                'msg' => $e->getMessage(),
                'code' => 1
            );

            return response()->json($response, 500);

        }
    }

